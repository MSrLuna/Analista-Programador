// Listas (arrays)
let frutas = ["manzana", "banana", "uva"];
console.log(frutas[0]);  // Acceso al primer elemento

// Diccionarios (objetos)
let persona = {
    nombre: "Carlos",
    edad: 30,
    profesion: "ingeniero"
};
console.log(persona.nombre);  // Acceso al valor por clave

//En JavaScript, las listas se llaman "arrays", y los diccionarios se implementan como objetos.
//Los objetos pueden tener propiedades (como "nombre", "edad" y "profesion" en este caso), que se pueden acceder utilizando la notación de punto o la notación de corchetes.