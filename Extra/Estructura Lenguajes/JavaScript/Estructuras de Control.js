//Condicionales:

let edad = 18;

if (edad >= 18) {
    console.log("Eres mayor de edad.");
} else {
    console.log("Eres menor de edad.");
}

//Las estructuras condicionales en JavaScript funcionan de manera similar a Python.
//El código dentro del bloque if se ejecuta si la condición es verdadera; de lo contrario, se ejecuta el bloque else.

//Bucles:

// Bucle for para iterar sobre una lista
let numeros = [1, 2, 3, 4, 5];
for (let i = 0; i < numeros.length; i++) {
    console.log(numeros[i]);
}

// Bucle while
let contador = 0;
while (contador < 5) {
    console.log("Contador:", contador);
    contador++;
}

//Los bucles en JavaScript también son similares a Python.
//For se utiliza para iterar sobre un conjunto de elementos, y while se utiliza para repetir un bloque de código mientras se cumple una condición.