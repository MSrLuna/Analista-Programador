# Definición de una función
def saludar(nombre):
    return "¡Hola, " + nombre + "!"

# Llamada a la función
mensaje = saludar("María")
print(mensaje)

#Las funciones permiten agrupar un conjunto de instrucciones para realizar una tarea específica.
#Las funciones pueden tomar argumentos (como nombre en este caso) y pueden devolver un valor utilizando la palabra clave return.