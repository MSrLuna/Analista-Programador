#Condicionales:

edad = 18

if edad >= 18:
    print("Eres mayor de edad.")
else:
    print("Eres menor de edad.")

#Las estructuras condicionales, como if, se utilizan para tomar decisiones en función de una condición.
#Si la condición es verdadera, el código dentro del bloque if se ejecutará; de lo contrario, se ejecutará el bloque else.

#Bucles:

# Bucle for para iterar sobre una lista
numeros = [1, 2, 3, 4, 5]
for num in numeros:
    print(num)

# Bucle while
contador = 0
while contador < 5:
    print("Contador:", contador)
    contador += 1

#Los bucles permiten ejecutar una serie de instrucciones repetidamente. 
#For se utiliza para iterar sobre una secuencia (como una lista) y while se utiliza cuando se quiere repetir un bloque de código mientras se cumple una condición.