// Listas (List)
val frutas = listOf("manzana", "banana")
println(frutas[0])  // Acceso al primer elemento

// Diccionarios (Map)
val persona = mapOf("nombre" to "Carlos", "edad" to 30)
println(persona["nombre"])  // Acceso al valor por clave

//En Kotlin, las listas se implementan utilizando la interfaz List (por ejemplo, List<String>), y los diccionarios se implementan utilizando la interfaz Map (por ejemplo, Map<String, Any>).
//Puedes acceder a elementos de una lista utilizando índices numéricos y a elementos de un diccionario utilizando claves.