# Listas (arrays)
frutas = ["manzana", "banana", "uva"]
print(frutas[0])  # Acceso al primer elemento

# Creación de una tupla
tupla = (1, 2, 3, 4, 5)
print(tupla[0])  # Acceso al primer elemento

# Diccionarios (mapas)
persona = {"nombre": "Carlos", "edad": 30, "profesion": "ingeniero"}
print(persona["nombre"])  # Acceso al valor por clave

#Las listas almacenan una colección ordenada de elementos, y los diccionarios almacenan pares clave-valor.
#Puedes acceder a elementos de una lista utilizando índices numéricos y a elementos de un diccionario utilizando claves.
#A diferencia de las listas, que se definen con corchetes [ ], las tuplas se definen con paréntesis ( ).
#Las tuplas son útiles cuando deseas almacenar una colección de valores que no cambiarán durante la ejecución del programa, como las coordenadas x e y de un punto en un plano.