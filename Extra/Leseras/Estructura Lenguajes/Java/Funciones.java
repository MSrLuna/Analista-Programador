// Definición de una función
public static String saludar(String nombre) {
    return "¡Hola, " + nombre + "!";
}

// Llamada a la función
String mensaje = saludar("María");
System.out.println(mensaje);

//Las funciones en Java permiten agrupar un conjunto de instrucciones para realizar una tarea específica.
