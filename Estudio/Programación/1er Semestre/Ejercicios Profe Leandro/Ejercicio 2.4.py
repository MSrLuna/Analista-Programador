#Crear un ALGORITMO que ingrese cualquier número, después multiplique por 4, ese resultado lo divide en 2. El resultado final debe ser mostrado en pantalla. 

# Pedir al usuario que ingrese un número y guardarlo en la variable "numero"
numero = float(input("Ingrese un número: "))

# Realizar la operación matemática para multiplicar el número ingresado por 4 y luego dividirlo por 2. Guardar el resultado en la variable "resultado"
resultado = (numero * 4) / 2

# Mostrar el resultado en la pantalla utilizando la función "print()"
print("El resultado es:", resultado)