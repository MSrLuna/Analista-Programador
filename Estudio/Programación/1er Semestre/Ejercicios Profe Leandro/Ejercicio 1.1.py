# Este programa pide al usuario que ingrese 5 números y luego calcula su suma

# Se solicita al usuario que ingrese el primer número y se convierte en un número de punto flotante
num1 = float(input("Ingresa el primer número: "))

# Se solicita al usuario que ingrese el segundo número y se convierte en un número de punto flotante
num2 = float(input("Ingresa el segundo número: "))

# Se solicita al usuario que ingrese el tercer número y se convierte en un número de punto flotante
num3 = float(input("Ingresa el tercer número: "))

# Se solicita al usuario que ingrese el cuarto número y se convierte en un número de punto flotante
num4 = float(input("Ingresa el cuarto número: "))

# Se solicita al usuario que ingrese el quinto número y se convierte en un número de punto flotante
num5 = float(input("Ingresa el quinto número: "))

# Se calcula la suma de los 5 números ingresados y se almacena en la variable "suma"
suma = num1 + num2 + num3 + num4 + num5

# Se muestra el resultado de la suma en la pantalla utilizando la función "print()"
print("La suma de los 5 números ingresados es:", suma)